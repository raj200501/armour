"""Generate Benchmark v3 with live external-agent traces and judge comparison."""

from __future__ import annotations

import json
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT))

from armour_labs.adjudication import build_adjudication_queue
from armour_labs.dataset import load_jsonl
from armour_labs.judge_baseline import evaluate_judge
from armour_labs.metrics import evaluate_labeled_records


LIVE_DATASET_PATH = ROOT / "datasets" / "live_external_agent_runs.jsonl"
BENCHMARK_DIR = ROOT / "benchmarks"
REPORT_PATH = BENCHMARK_DIR / "armour_agent_safety_v3.md"
SUMMARY_PATH = BENCHMARK_DIR / "armour_agent_safety_v3_summary.json"
ADJUDICATION_PATH = ROOT / "datasets" / "adjudication_queue_v3.jsonl"


def main() -> int:
    BENCHMARK_DIR.mkdir(parents=True, exist_ok=True)
    records = load_jsonl(LIVE_DATASET_PATH)
    monitor_metrics = evaluate_labeled_records(records)
    judge_metrics = evaluate_judge(records)
    adjudication_queue = build_adjudication_queue(records)
    ADJUDICATION_PATH.write_text(
        "".join(json.dumps(item, sort_keys=True) + "\n" for item in adjudication_queue),
        encoding="utf-8",
    )
    summary = {
        "created_at": datetime.now(timezone.utc).isoformat(),
        "suite": "armour-agent-safety-v3",
        "dataset": {
            "path": "datasets/live_external_agent_runs.jsonl",
            "records": len(records),
            "label_source": "operator-reviewed-live-fixture",
        },
        "monitor_metrics": monitor_metrics["overall"],
        "judge_baseline": {
            "name": "offline-rubric-judge",
            "note": "LLM-judge-style offline proxy; no model/API call.",
            "metrics": judge_metrics["overall"],
            "errors": judge_metrics["errors"],
        },
        "monitor_errors": monitor_metrics["errors"],
        "adjudication_queue": {
            "path": "datasets/adjudication_queue_v3.jsonl",
            "records": len(adjudication_queue),
        },
    }
    SUMMARY_PATH.write_text(json.dumps(summary, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    REPORT_PATH.write_text(_render_markdown(summary), encoding="utf-8")
    print(f"Wrote {REPORT_PATH}")
    print(f"Wrote {SUMMARY_PATH}")
    print(f"Wrote {ADJUDICATION_PATH}")
    return 0


def _render_markdown(summary: dict[str, Any]) -> str:
    monitor = summary["monitor_metrics"]
    judge = summary["judge_baseline"]["metrics"]
    monitor_error_rows = "\n".join(_monitor_error_row(row) for row in summary["monitor_errors"])
    judge_error_rows = "\n".join(_judge_error_row(row) for row in summary["judge_baseline"]["errors"])
    if not monitor_error_rows:
        monitor_error_rows = "| none | none | none | none |"
    if not judge_error_rows:
        judge_error_rows = "| none | none | none | none |"
    return f"""# Armour Agent Safety Benchmark v3

Generated: {summary["created_at"]}

Benchmark v3 adds live traces generated by a separate external MCP-style agent
harness. It also compares Armour's rule monitor against an offline rubric judge
that approximates the structure of an LLM-as-judge baseline without making API
calls.

## Dataset

- Dataset path: `{summary["dataset"]["path"]}`
- Records: {summary["dataset"]["records"]}
- Label source: `{summary["dataset"]["label_source"]}`
- Adjudication queue: `{summary["adjudication_queue"]["path"]}`
- Adjudication records: {summary["adjudication_queue"]["records"]}

## Monitor vs Judge Baseline

| System | Records | TP | FP | TN | FN | Precision | Recall | Specificity | Accuracy | F1 |
|---|---:|---:|---:|---:|---:|---:|---:|---:|---:|---:|
| Armour rule monitor | {monitor["records"]} | {monitor["true_positive"]} | {monitor["false_positive"]} | {monitor["true_negative"]} | {monitor["false_negative"]} | {_pct(monitor["precision"])} | {_pct(monitor["recall"])} | {_pct(monitor["specificity"])} | {_pct(monitor["accuracy"])} | {_pct(monitor["f1"])} |
| offline rubric judge | {judge["records"]} | {judge["true_positive"]} | {judge["false_positive"]} | {judge["true_negative"]} | {judge["false_negative"]} | {_pct(judge["precision"])} | {_pct(judge["recall"])} | {_pct(judge["specificity"])} | {_pct(judge["accuracy"])} | {_pct(judge["f1"])} |

## Armour Monitor Errors

| Record | Label | Verdict | Finding Rules | Review Notes |
|---|---|---|---|---|
{monitor_error_rows}

## Offline Judge Errors

| Record | Label | Predicted Risky | Rationale |
|---|---|---|---|
{judge_error_rows}

## What This Adds

- Live traces are collected by executing `examples/external_agents/tiny_mcp_agent.py`
  as a subprocess.
- The benchmark compares two independent scoring paths: Armour monitor rules
  and an offline judge-style rubric.
- Disagreements are exported to `datasets/adjudication_queue_v3.jsonl`.

## Limitations

- The external agent harness is deterministic and offline; it is not a frontier
  model or hosted agent.
- The judge baseline is an offline proxy, not an actual LLM call.
- Labels are operator-reviewed fixtures, not an independent human study.

## Next Benchmark

Benchmark v4 should run the same adapters on traces from a real model-backed
agent and replace the offline judge proxy with a real LLM-as-judge baseline.
"""


def _monitor_error_row(row: dict[str, Any]) -> str:
    rules = ", ".join(row["finding_rules"]) if row["finding_rules"] else "none"
    return (
        f"| `{row['id']}` | `{row['label']}` | `{row['verdict']}` | "
        f"{rules} | {_clean(row['review_notes'])} |"
    )


def _judge_error_row(row: dict[str, Any]) -> str:
    return (
        f"| `{row['id']}` | `{row['label']}` | `{row['predicted_risky']}` | "
        f"{_clean(row['rationale'])} |"
    )


def _clean(text: str) -> str:
    return " ".join(text.split()).replace("|", "/")


def _pct(value: float) -> str:
    return f"{value * 100:.1f}%"


if __name__ == "__main__":
    raise SystemExit(main())
